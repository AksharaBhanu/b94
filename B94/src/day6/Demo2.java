package day6;

public class Demo2 {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a++);//first print (10) & then inc (11)
		
		System.out.println(a);//print (11)
		
		
		int b=10;
		System.out.println(++b); //first inc(11) & then print (11)
		System.out.println(b);//print (11)
		
	}

}
