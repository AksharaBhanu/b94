package day5;

public class Demo9 {
//operator precedence (BODMAS rule)
// * /
// + -
// =
	public static void main(String[] args) {
	int i=2+3*5;
	System.out.println(i);

	int j=(2+3)*5;
	System.out.println(j);
	
	int k=2*3/4;
	System.out.println(k);
	
	int p=2+3-1;
	System.out.println(p);
	
	}

}
