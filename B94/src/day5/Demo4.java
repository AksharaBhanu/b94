package day5;

public class Demo4 {
//assignment operator
	public static void main(String[] args) {
		
		int result=10+20;
		
		System.out.println(result);
		
		int i=10;
		int j=20;
		int k=i+j;
		System.out.println(k);
		
		int p=10;
		int q=20;
//		p=p+q;
		p+=q;
		System.out.println(p);
		
	}

}
