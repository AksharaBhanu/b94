package day5;

public class Demo7 {

	public static void main(String[] args) {
		
		int i=10;
		i=i+1;
		System.out.println(i);
		
		i=10;
		i+=1;
		System.out.println(i);
		
		i=10;
		i++;//post increment
		System.out.println(i);
		
		i=10;
		++i;//per increment
		System.out.println(i);
		
		i=10;
		i=i-1;
		System.out.println(i);
		
		i=10;
		i-=1;
		System.out.println(i);
		
		i=10;
		i--;//post decrement
		System.out.println(i);
		
		i=10;
		--i;//pre decrement
		System.out.println(i);
		
	}
}
